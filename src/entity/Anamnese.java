package entity;

public class Anamnese {


    private int id;


    private String queixasPrincipais;


    private String atencendentes;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getQueixasPrincipais() {
        return queixasPrincipais;
    }

    public void setQueixasPrincipais(String queixasPrincipais) {
        this.queixasPrincipais = queixasPrincipais;
    }

    public String getAtencendentes() {
        return atencendentes;
    }

    public void setAtencendentes(String atencendentes) {
        this.atencendentes = atencendentes;
    }
}
